EN_TO_TR_TRANSLATIONS: dict[str, str] = {v: k for k, v in TRANSLATIONS.items()}


class LanguageManager:
    def __init__(self) -> None:
        self._lang = LANG_TR
        self._listeners: list[Callable[[str], None]] = []

    @property
    def language(self) -> str:
        return self._lang

    def set_language(self, lang: str) -> None:
        if lang not in (LANG_TR, LANG_EN) or self._lang == lang:
            return
        self._lang = lang
        for listener in list(self._listeners):
            listener(lang)

    def tr(self, text: str) -> str:
        if self._lang == LANG_TR:
            return text
        return TRANSLATIONS.get(text, text)

    def translate(self, text: str, lang: str) -> str:
        if lang == LANG_TR:
            return EN_TO_TR_TRANSLATIONS.get(text, text)
        return TRANSLATIONS.get(text, text)

    def db_error(self, msg: str) -> str:
        if self._lang == LANG_TR:
            return msg
        return DB_ERROR_TRANSLATIONS.get(msg, msg)

    def subscribe(self, listener: Callable[[str], None]) -> None:
        if listener not in self._listeners:
            self._listeners.append(listener)


lang_manager = LanguageManager()


def apply_language_to_widget(root: QWidget, lang: str) -> None:
    def _apply_label(lbl: QLabel) -> None:
        lbl.setText(lang_manager.translate(lbl.text(), lang))
    def _apply_button(btn: QPushButton) -> None:
        btn.setText(lang_manager.translate(btn.text(), lang))
        tip = btn.toolTip()
        if tip:
            btn.setToolTip(lang_manager.translate(tip, lang))
    def _apply_line_edit(le: QLineEdit) -> None:
        le.setPlaceholderText(lang_manager.translate(le.placeholderText(), lang))

    if root.windowTitle():
        root.setWindowTitle(lang_manager.translate(root.windowTitle(), lang))

    for lbl in root.findChildren(QLabel): _apply_label(lbl)
    for btn in root.findChildren(QPushButton): _apply_button(btn)
    for le in root.findChildren(QLineEdit): _apply_line_edit(le)
    
    # BÜTÜN TABLO BAŞLIKLARINI OTOMATİK ÇEVİRECEK KISIM:
    from PyQt6.QtWidgets import QTableWidget
    for tbl in root.findChildren(QTableWidget):
        for i in range(tbl.columnCount()):
            item = tbl.horizontalHeaderItem(i)
            if item:
                item.setText(lang_manager.translate(item.text(), lang))